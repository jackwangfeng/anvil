use crate::span::Span;

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum TokenKind {
    KwInt,
    KwReturn,
    Ident(String),
    IntLit(i64),
    StrLit(String),
    LParen,
    RParen,
    LBrace,
    RBrace,
    Semicolon,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Comma,
    Amp,
    LBracket,
    RBracket,
    Dot,
    Ellipsis,
    Arrow,
    Assign,
    Pipe,
    Caret,
    Tilde,
    Shl,
    Shr,
    AmpAmp,
    PipePipe,
    Bang,
    Question,
    Colon,
    PlusPlus,
    MinusMinus,
    PlusEq,
    MinusEq,
    StarEq,
    SlashEq,
    PercentEq,
    AmpEq,
    PipeEq,
    CaretEq,
    ShlEq,
    ShrEq,
    Lt,
    Gt,
    Le,
    Ge,
    EqEq,
    NotEq,
    KwIf,
    KwElse,
    KwWhile,
    KwFor,
    KwChar,
    KwSizeof,
    KwStruct,
    KwUnion,
    KwEnum,
    KwTypedef,
    KwVoid,
    KwConst,
    KwBreak,
    KwContinue,
    KwSwitch,
    KwCase,
    KwDefault,
    Eof,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}
