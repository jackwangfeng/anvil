use crate::span::Span;

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum TokenKind {
    KwInt,
    KwReturn,
    Ident(String),
    IntLit(i64),
    StrLit(String),
    LParen,
    RParen,
    LBrace,
    RBrace,
    Semicolon,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Comma,
    Amp,
    LBracket,
    RBracket,
    Dot,
    Arrow,
    Assign,
    Lt,
    Gt,
    Le,
    Ge,
    EqEq,
    NotEq,
    KwIf,
    KwElse,
    KwWhile,
    KwFor,
    KwChar,
    KwSizeof,
    KwStruct,
    KwUnion,
    KwEnum,
    KwTypedef,
    Eof,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}
